"""
Goooood students
Вам доступен текстовый файл grades.txt, содержащий оценки студента за три теста в
каждом из триместров. Строки файла имеют вид: фамилия оценка_1 оценка_2 оценка_3.

Напишите программу для подсчета количества студентов, сдавших все три теста. Тест
считается сданным, если количество баллов по нему не меньше 6565.

Формат входных данных
На вход программе ничего не подается.

Формат выходных данных
Программа должна вывести количество студентов, сдавших все три теста.

Примечание 1. Считайте, что исполняемая программа и указанный файл находятся в одной папке.

Примечание 2. Если бы файл grades.txt содержал строки:

Washington 83 77 54
Adams 86 69 90
Jacobson 50 49 71
MacDonald 100 99 100
Berrington 66 67 64
то результатом будет:

2
"""


with open('files/grades.txt', encoding='utf-8') as inf:
    res = 0
    for line in inf:
        name, score1, score2, score3 = line.split()
        if int(score1) >= 65 and int(score2) >= 65 and int(score3) >= 65:
            res += 1
print(res)
